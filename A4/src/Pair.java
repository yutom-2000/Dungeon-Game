/**
 * Represents the pair of two Strings.
 */
public class Pair {

  private String first;
  private String second;

  public Pair(String first, String second) {
    this.first = first;
    this.second = second;
  }

  public String getFirst() {
    return this.first;
  }

  public String getSecond() {
    return this.second;
  }
}
